def clean_json_response(text: str) -> str:
    """Очищает ответ от Markdown разметки."""
    text = text.strip()
    if text.startswith("```json"):
        text = text[7:]
    if text.startswith("```"):
        text = text[3:]
    if text.endswith("```"):
        text = text[:-3]
    return text.strip()


def generate_questions_with_context(context_data):
    topic = context_data['topic']
    legal_text = context_data['legal_text']

    generated_batch = []

    for q_type in context_data['q_types']:
        count = 5
        logger.info(f"🚀 Groq генерирует: {topic} -> {q_type}")

        prompt = f"""
        Ты — эксперт по праву и педагог. Целевая аудитория: школьники 8-16 лет.
        Твоя задача: создать {count} уникальных вопросов в формате JSON на основе ПРЕДОСТАВЛЕННОГО ТЕКСТА ЗАКОНА.

        ВХОДНОЙ ТЕКСТ ЗАКОНА (КОНТЕКСТ):
        "{legal_text}"
        
        ТРЕБОВАНИЯ К ВОПРОСАМ
        1. Создавай вопросы в виде жизненных мини-ситуаций (кейсов), понятных детям. 
        2. Создавай вопросы в игровой форме. Речь простая, понятная. Поменьше используй сухого юридического языка, упрощай сложные моменты, но сохраняй точность.
        3. Создавай простые вопросы. Считай, что у пользователя (ребёнка) нет того контекста, который я тебе предоставил. 
        4. Верные ответы должны просто и очевидно находиться, остальны варианты ответа должны однозначно трактоваться как неверные.
        5. Не задавай вопросы, затрагивающие сложные и малозначимые аспекты, проверяется общее понимание законов, а не их тонкостей.
        Например, не нужно спрашивать про конкретный размер штрафа за то или иное правонарушение, так как ребёнку это попросту не нужно знать.
        6. Не умоминай в вопросах номера статей, глав и в целом документы, которые ты получаешь в качестве контекста
        
        ТРЕБОВАНИЯ К ФОРМАТУ JSON (СТРОГО):
        1. Используй ТОЛЬКО факты из текста выше.
        2. Язык: Русский.
        3. Тип вопроса: {q_type}.
        4. Структура JSON должна В ТОЧНОСТИ совпадать с этим шаблоном: 
        {TEMPLATES[q_type]}

        ОСОБЫЕ ТРЕБОВАНИЯ ПО ПОЛЯМ:
        - Поле "hint" ОБЯЗАТЕЛЬНО: краткая подсказка, частично раскрывающая правильный ответ, но не дающая его полностью.
        - Для 'single': поле "correct" должно быть целым числом (индекс правильного ответа). Обязательно только один правильный ответ, все остальные однозначно неверные.
        - Для 'multiple': поле "correct" должно быть массивом чисел (индексы). Обязательно должно быть два и более правильных ответа. Если правильный ответ один - вопрос ужасен. Недопустимы вопросы с ответом "Всё вышеперечисленное" и подобное
        - Для 'fill-blanks': поле "code" должно содержать HTML теги <span class="blank" data-id="X">______</span>. Экранируй кавычки внутри JSON корректно.

        ВАЖНО: Верни ТОЛЬКО валидный JSON массив: [{TEMPLATES[q_type]}, ...]. 
        Без лишних слов.
        """

        headers = {
            "Authorization": f"Bearer {GROQ_API_KEY}",
            "Content-Type": "application/json"
        }

        data = {
            "model": MODEL,
            "messages": [
                {"role": "system",
                 "content": "Ты полезный ассистент, который всегда отвечает только валидным JSON массивом."},
                {"role": "user", "content": prompt}
            ],
            "temperature": 0.4,
            "max_tokens": 4096
        }

        retries = 0
        success = False

        while retries < MAX_RETRIES and not success:
            try:
                response = requests.post(GROQ_API_URL, headers=headers, json=data)
                response.raise_for_status()

                result = response.json()
                content = result['choices'][0]['message']['content']

                cleaned_content = clean_json_response(content)
                result_json = json.loads(cleaned_content)

                generated_batch.extend(result_json)
                success = True

                time.sleep(5)

            except requests.exceptions.HTTPError as e:
                if response.status_code == 429:
                    error_msg = response.text
                    wait_time = 0
                    match = re.search(r"try again in (\d+\.?\d*)s", error_msg)
                    if match:
                        wait_time = float(match.group(1)) + 1
                    else:
                        wait_time = BASE_DELAY * (2 ** retries)

                    logger.warning(
                        f"⚠️ Лимит запросов (429). Ждем {wait_time:.2f} сек... (Попытка {retries + 1}/{MAX_RETRIES})")
                    time.sleep(wait_time)
                    retries += 1
                else:
                    logger.error(f"❌ Ошибка Groq ({q_type}): {e}")
                    logger.error(f"Детали ошибки API: {response.text}")
                    break
            except Exception as e:
                logger.error(f"❌ Непредвиденная ошибка ({q_type}): {e}")
                # Если ошибка парсинга JSON, можно попробовать еще раз (иногда модель сбоит)
                retries += 1
                time.sleep(2)

        if not success:
            logger.error(f"⛔ Не удалось сгенерировать {q_type} после {MAX_RETRIES} попыток.")

    return generated_batch


def main():
    all_questions = []

    # Загружаем существующие, если есть
    if os.path.exists(OUTPUT_FILE):
        try:
            with open(OUTPUT_FILE, 'r', encoding='utf-8') as f:
                all_questions = json.load(f)
                logger.info(f"Загружено {len(all_questions)} вопросов из файла.")
        except:
            pass

    # Проходим по реестру контекстов
    for context_entry in CONTEXT_REGISTRY:
        new_q = generate_questions_with_context(context_entry)
        if new_q:
            all_questions.extend(new_q)
            with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
                json.dump(all_questions, f, ensure_ascii=False, indent=2)
            logger.info(f"✅ Успешно сохранено +{len(new_q)} вопросов.")

    logger.info(f"\n🎉 Генерация завершена! Всего вопросов в базе: {len(all_questions)}")


if __name__ == "__main__":
    main()